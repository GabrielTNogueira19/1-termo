# Como usar o git em outras máquinas:
# 1. Clone o repositório: Use o comando "git clone" seguido do URL do repositório para clonar o projeto em sua máquina local.

# 2. Configure o Git: Configure seu nome de usuário e endereço de e-mail usando os comandos "git config --global user.name" e "git config --global user.email".
# Isso é importante para identificar suas contribuições no histórico do projeto.

# 3. Faça alterações e commit: Faça as alterações necessárias no código e use o comando "git add" para adicionar os arquivos modificados ao estágio de preparação.
# Em seguida, use "git commit" para criar um commit com uma mensagem descritiva das alterações feitas.

# 4. Envie as alterações para o repositório remoto: Use o comando "git push" para enviar suas alterações para o repositório remoto, permitindo que outros colaboradores 
# vejam e integrem suas mudanças.